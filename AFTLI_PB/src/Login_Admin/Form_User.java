    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:\
     String username = jTextField1.getText().trim();
        String rawPassword = new String(jPasswordField1.getPassword()).trim();
        String akses = (String) jComboBox1.getSelectedItem();

        if (username.isEmpty() || rawPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong.");
            return;
        }

        if (rawPassword.length() < 6) {
            JOptionPane.showMessageDialog(this, "Password minimal 6 karakter.");
            return;
        }

        String password = HashUtil.hashPassword(rawPassword); // pastikan HashUtil kamu tersedia

        try (Connection conn = DatabaseConnection.connect();
             PreparedStatement cekStmt = conn.prepareStatement("SELECT * FROM login WHERE username = ?")) {
            cekStmt.setString(1, username);
            ResultSet rs = cekStmt.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Username sudah terdaftar.");
                return;
            }

            String insertQuery = "INSERT INTO login(username, password, akses) VALUES (?, ?, ?)";
            try (PreparedStatement stmt = conn.prepareStatement(insertQuery)) {
                stmt.setString(1, username);
                stmt.setString(2, password);
                stmt.setString(3, akses);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "User berhasil ditambahkan.");
                tampilkanDataUser();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

